package com.example.hastanghubaga.ui.tokens

import androidx.compose.ui.unit.dp

/**
 * Centralized spacing tokens.
 *
 * WHY:
 * - Prevent magic numbers
 * - Ensure visual consistency
 * - Easy global tuning
 */
object Dimens {

    // Micro spacing (icons, tight layouts)
    val SpaceXS = 4.dp
    val SpaceS = 8.dp

    // Standard spacing
    val SpaceM = 16.dp
    val SpaceL = 24.dp

    // Large spacing (screen padding, sections)
    val SpaceXL = 32.dp
    val SpaceXXL = 48.dp
}
